package com.hut.xj.SuperMarket.domain;

import java.util.Date;

public class Supplier {
    private String supplier_num;
    private String supplier_name;
    private String supplier_contacts;
    private String supplier_c_phone;
    private String supplier_address;
    private String supplier_fax;
    private String supplier_des;
    private String supplier_time;

    public String getSupplier_des() {
        return supplier_des;
    }

    public void setSupplier_des(String supplier_des) {
        this.supplier_des = supplier_des;
    }

    public String getSupplier_address() {
        return supplier_address;
    }

    public void setSupplier_address(String supplier_address) {
        this.supplier_address = supplier_address;
    }

    public String getSupplier_num() {
        return supplier_num;
    }

    public void setSupplier_num(String supplier_num) {
        this.supplier_num = supplier_num;
    }

    public String getSupplier_name() {
        return supplier_name;
    }

    public void setSupplier_name(String supplier_name) {
        this.supplier_name = supplier_name;
    }

    public String getSupplier_contacts() {
        return supplier_contacts;
    }

    public void setSupplier_contacts(String supplier_contacts) {
        this.supplier_contacts = supplier_contacts;
    }

    public String getSupplier_c_phone() {
        return supplier_c_phone;
    }

    public void setSupplier_c_phone(String supplier_c_phone) {
        this.supplier_c_phone = supplier_c_phone;
    }

    public String getSupplier_fax() {
        return supplier_fax;
    }

    public void setSupplier_fax(String supplier_fax) {
        this.supplier_fax = supplier_fax;
    }

    public String getSupplier_time() {
        return supplier_time;
    }

    public void setSupplier_time(String supplier_time) {
        this.supplier_time = supplier_time;
    }
}
